function Change_Level (Level_Number: number) {
    if (Level_Number == 1) {
        tiles.setCurrentTilemap(tilemap`level2`)
    } else if (Level_Number == 2) {
        tiles.setCurrentTilemap(tilemap`level2`)
    } else if (Level_Number == 3) {
        tiles.setCurrentTilemap(tilemap`level12`)
    } else if (Level_Number == 4) {
        tiles.setCurrentTilemap(tilemap`level14`)
    } else if (Level_Number == 5) {
        tiles.setCurrentTilemap(tilemap`level17`)
    } else if (Level_Number == 6) {
        tiles.setCurrentTilemap(tilemap`level19`)
    } else if (Level_Number == 7) {
        tiles.setCurrentTilemap(tilemap`level21`)
    } else if (Level_Number == 8) {
        tiles.setCurrentTilemap(tilemap`level23`)
        scene.setBackgroundColor(7)
    } else if (Level_Number == 9) {
        tiles.setCurrentTilemap(tilemap`level23`)
        scene.setBackgroundColor(7)
    } else if (Level_Number == 10) {
        tiles.setCurrentTilemap(tilemap`level29`)
        scene.setBackgroundColor(4)
    } else if (Level_Number == 11) {
        tiles.setCurrentTilemap(tilemap`level32`)
    }
    tiles.placeOnRandomTile(Link, sprites.dungeon.floorLight0)
}
